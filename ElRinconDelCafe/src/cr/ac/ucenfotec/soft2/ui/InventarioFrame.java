    public void cargarProductosEnTabla() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        
        for (Producto p : inventario.obtenerTodosLosProductos()) {
            Object[] fila = {
                p.getCodigoProducto(),
                p.getNombreProducto(),
                p.getCategoriaProducto(),
                p.getPrecioProducto(),
                p.getCantidadStock()
            };
            modelo.addRow(fila);
        }
    }

