    private void botonAgregarProductosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarProductosActionPerformed
        String codigo = JOptionPane.showInputDialog("Código del producto:");
        String nombre = JOptionPane.showInputDialog("Nombre del producto:");
        String tipo = JOptionPane.showInputDialog("Tipo (Café, Bebida Fría, Pastelería):");
        double precio = Double.parseDouble(JOptionPane.showInputDialog("Precio:"));
        int cantidad = Integer.parseInt(JOptionPane.showInputDialog("Cantidad en stock:"));

        Producto nuevo = new Producto(codigo, nombre, tipo, precio, cantidad);
        inventario.agregarProducto(nuevo);
        JOptionPane.showMessageDialog(this, "Producto agregado correctamente.");
    }//GEN-LAST:event_botonAgregarProductosActionPerformed

    private void botonModificarProductosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModificarProductosActionPerformed
        // TODO add your handling code here:
        String codigo = JOptionPane.showInputDialog("Código del producto a modificar:");
        Producto p = inventario.buscarProducto(codigo);
        if (p != null) {
            p.setNombreProducto(JOptionPane.showInputDialog("Nuevo nombre:", p.getNombreProducto()));
            p.setCategoriaProducto(JOptionPane.showInputDialog("Nuevo tipo:", p.getCategoriaProducto()));
            p.setPrecioProducto(Double.parseDouble(JOptionPane.showInputDialog("Nuevo precio:", p.getPrecioProducto())));
            p.setCantidadStock(Integer.parseInt(JOptionPane.showInputDialog("Nueva cantidad:", p.getCantidadStock())));
            JOptionPane.showMessageDialog(this, "Producto modificado correctamente.");
        } else {
            JOptionPane.showMessageDialog(this, "Producto no encontrado.");
        }
    }//GEN-LAST:event_botonModificarProductosActionPerformed

