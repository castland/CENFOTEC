    public void cargarClientesEnTabla() {
        DefaultTableModel modelo = (DefaultTableModel) jTableClientes.getModel();
        modelo.setRowCount(0);

        for (Cliente c : gestor.getClientes()) {
            Object[] fila = {
                c.getNombre(),
                c.getApellido(),
                c.getCedula(),
                c.getCorreo()
            };
            modelo.addRow(fila);
        }
    }

