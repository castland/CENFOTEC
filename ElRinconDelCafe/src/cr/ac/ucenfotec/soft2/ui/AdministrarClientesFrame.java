    private void botonEliminarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarClienteActionPerformed
        int filaSeleccionada = jTableClientes.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un cliente para eliminar.");
            return;
        }

        String cedulaCliente = jTableClientes.getValueAt(filaSeleccionada, 2).toString();
        String nombreCliente = jTableClientes.getValueAt(filaSeleccionada, 0).toString();
        String apellidoCliente = jTableClientes.getValueAt(filaSeleccionada, 1).toString();

        int confirmacion = JOptionPane.showConfirmDialog(
                this,
                "¿Está seguro que desea eliminar al cliente " + nombreCliente + " " + apellidoCliente + "?",
                "Confirmar Eliminación",
                JOptionPane.YES_NO_OPTION
        );

        if (confirmacion == JOptionPane.YES_OPTION) {
            if (gestor.eliminarCliente(cedulaCliente)) {
                cargarClientesEnTabla();
                JOptionPane.showMessageDialog(this, "Cliente eliminado correctamente.");
            }
        }
    }//GEN-LAST:event_botonEliminarClienteActionPerformed

