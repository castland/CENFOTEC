    private void botonBuscarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarClienteActionPerformed
        String cedula = txtBuscarCedula.getText().trim();

        if (cedula.isEmpty()) {
            cargarClientesEnTabla(); // Mostrar todos
            return;
        }

        Cliente cliente = gestor.buscarCliente(cedula);
        DefaultTableModel modelo = (DefaultTableModel) jTableClientes.getModel();
        modelo.setRowCount(0);

        if (cliente != null) {
            Object[] fila = {cliente.getNombre(), cliente.getApellido(), cliente.getCedula(), cliente.getCorreo()};
            modelo.addRow(fila);
        } else {
            JOptionPane.showMessageDialog(this, "Cliente no encontrado.");
        }
    }//GEN-LAST:event_botonBuscarClienteActionPerformed

