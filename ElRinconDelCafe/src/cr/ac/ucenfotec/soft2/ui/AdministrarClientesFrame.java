    private void botonModificarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModificarClienteActionPerformed
        int filaSeleccionada = jTableClientes.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un cliente para modificar.");
            return;
        }

        String cedula = jTableClientes.getValueAt(filaSeleccionada, 2).toString();
        Cliente cliente = gestor.buscarCliente(cedula);

        if (cliente != null) {
            this.setEnabled(false);

            AgregarNuevoClienteFrame frame = new AgregarNuevoClienteFrame(gestor, this, cliente);

            frame.addWindowListener(new java.awt.event.WindowAdapter() {
                @Override
                public void windowClosed(java.awt.event.WindowEvent e) {
                    AdministrarClientesFrame.this.setEnabled(true);
                    AdministrarClientesFrame.this.toFront();
                    AdministrarClientesFrame.this.requestFocus();
                }
            });

            frame.setVisible(true);
        }
    }//GEN-LAST:event_botonModificarClienteActionPerformed

