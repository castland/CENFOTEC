    public void cargarUsuariosEnTabla(GestorUsuarios gestor) {
        DefaultTableModel modelo = (DefaultTableModel) jTableUsuarios.getModel();
        modelo.setRowCount(0); //LIMPIAR TABLA

        for (Usuario u : gestor.getUsuarios()) {
            Object[] fila = {
                u.getNombre(),
                u.getApellido(),
                u.getCedula(),
                u.getRol()
            };
            modelo.addRow(fila);
        }
    }

