    private void botonEliminarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarUsuarioActionPerformed
        int filaSeleccionada = jTableUsuarios.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un usuario para eliminar.");
            return;
        }

        // Obtener la cédula del usuario seleccionado
        String cedulaUsuario = jTableUsuarios.getValueAt(filaSeleccionada, 2).toString();
        String nombreUsuario = jTableUsuarios.getValueAt(filaSeleccionada, 0).toString();

        // Confirmar eliminación
        int confirmacion = JOptionPane.showConfirmDialog(
                this,
                "¿Está seguro que desea eliminar al usuario " + nombreUsuario + "?",
                "Confirmar Eliminación",
                JOptionPane.YES_NO_OPTION
        );

        if (confirmacion == JOptionPane.YES_OPTION) {
            // Buscar y eliminar el usuario de la lista
            gestor.getUsuarios().removeIf(usuario -> usuario.getCedula().equals(cedulaUsuario));

            // Actualizar la tabla
            cargarUsuariosEnTabla(gestor);

            JOptionPane.showMessageDialog(this, "Usuario eliminado correctamente.");
        }
    }//GEN-LAST:event_botonEliminarUsuarioActionPerformed

    private void botonModificarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModificarUsuarioActionPerformed
        int filaSeleccionada = jTableUsuarios.getSelectedRow();
        
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un usuario para modificar.");
            return;
        }
        
        String cedula = jTableUsuarios.getValueAt(filaSeleccionada, 2).toString();
        Usuario usuario = null;
        
        // Buscar el usuario por cédula
        for (Usuario u : gestor.getUsuarios()) {
            if (u.getCedula().equals(cedula)) {
                usuario = u;
                break;
            }
        }
        
        if (usuario != null) {
            this.setEnabled(false);
            
            AgregarNuevoUsuarioFrame frame = new AgregarNuevoUsuarioFrame(gestor, this, usuario);
            
            frame.addWindowListener(new java.awt.event.WindowAdapter() {
                @Override
                public void windowClosed(java.awt.event.WindowEvent e) {
                    AdministrarUsuariosFrame.this.setEnabled(true);
                    AdministrarUsuariosFrame.this.toFront();
                    AdministrarUsuariosFrame.this.requestFocus();
                }
            });
            
            frame.setVisible(true);
        }
    }//GEN-LAST:event_botonModificarUsuarioActionPerformed

